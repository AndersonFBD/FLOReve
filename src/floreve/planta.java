package floreve;

public class planta {
    int id; 
    String nomePop, nomeCie;
    double preco, tamanho;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNomePop() {
        return nomePop;
    }

    public void setNomePop(String nomePop) {
        this.nomePop = nomePop;
    }

    public String getNomeCie() {
        return nomeCie;
    }

    public void setNomeCie(String nomeCie) {
        this.nomeCie = nomeCie;
    }

    public double getPreco() {
        return preco;
    }

    public void setPreco(double preco) {
        this.preco = preco;
    }

    public double getTamanho() {
        return tamanho;
    }

    public void setTamanho(double tamanho) {
        this.tamanho = tamanho;
    }
    
    
    
}
