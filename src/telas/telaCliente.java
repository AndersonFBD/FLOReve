    private void bt_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_removerActionPerformed
        cli.c1.setId(Integer.parseInt(tf_idCliente.getText()));
        cli.localizar();
        int n = JOptionPane.showConfirmDialog(null,"deseja apagar o cliente de registro " +cli.c1.getId()+" dos registros?", "remover cliente?", JOptionPane.YES_NO_OPTION);
        if(n==JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, "removido! " +cli.atualizar(clienteDAO.EXCLUSAO));
            limpaCampos();
            habilitar(true, false, false, false, false, false);
        }
    }//GEN-LAST:event_bt_removerActionPerformed

    private void bt_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarActionPerformed
        if(tf_idCliente.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "preencha o campo id");
        } else {
            habilitar(true,true,true,true,true,true);
        }
    }//GEN-LAST:event_bt_editarActionPerformed

