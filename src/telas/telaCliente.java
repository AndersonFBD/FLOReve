    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed

        cli.c1.setId(Integer.parseInt(tf_idCliente.getText()));
        cli.c1.setNome(tf_nome.getText());
        cli.c1.setTelefone(tf_telefone.getText());
        cli.c1.setEndereco(tf_endereco.getText());
        JOptionPane.showMessageDialog(null, "dados atualizados." +cli.atualizar(clienteDAO.ALTERACAO));
        limpaCampos();
        habilitar(true, false, false, false, false, false);
    }//GEN-LAST:event_bt_salvarActionPerformed

