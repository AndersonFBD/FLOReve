    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed

        prod.p1.setMarca(tf_marca.getText());
        prod.p1.setNome(tf_nome.getText());
        prod.p1.setPreco(Double.parseDouble(tf_valor.getText()));
        prod.p1.setTipo(tf_tipo.getText());
        JOptionPane.showMessageDialog(null, "dados atualizados." +prod.atualizar(produtosDAO.ALTERACAO));
        limpaCampos();
        habilitar(true, false, false, false, false, true, false, false);
    }//GEN-LAST:event_bt_salvarActionPerformed

