    private void bt_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_removerActionPerformed
        prod.p1.setId(Integer.parseInt(tf_idProduto.getText()));
        prod.localizar();
        int n = JOptionPane.showConfirmDialog(null,"deseja apagar o produto de registro " +prod.p1.getId()+" dos registros?", "remover produto?", JOptionPane.YES_NO_OPTION);
        if(n==JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, "removido! " +prod.atualizar(produtosDAO.EXCLUSAO));
            limpaCampos();
            habilitar(true, false, false, false, false, true, false, false);
        }
    }//GEN-LAST:event_bt_removerActionPerformed

    private void bt_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarActionPerformed
        if(tf_idProduto.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "preencha o campo id");
        } else {
            habilitar(true,true,true,true,true,true,true,true);
        }
    }//GEN-LAST:event_bt_editarActionPerformed

