        public void habilitar(boolean tidP, boolean tNome, boolean tMarca, boolean tTipo,
                boolean tvalor, boolean beditar, boolean bremover ,boolean bsalvar){
            tf_idProduto.setEnabled(tidP);
            tf_marca.setEnabled(tMarca);
            tf_nome.setEnabled(tNome);
            tf_tipo.setEnabled(tTipo);
            tf_valor.setEnabled(tvalor);
            bt_editar.setEnabled(beditar);
            bt_remover.setEnabled(bremover);
            bt_salvar.setEnabled(bsalvar);            
    }
        
        void limpaCampos(){
            tf_idProduto.setText("");
            tf_marca.setText("");
            tf_nome.setText("");
            tf_tipo.setText("");
            tf_valor.setText("");
        }

        public void atualizarCampos(){
            prod.p1.setId(Integer.parseInt(tf_idProduto.getText()));
            if(prod.localizar()){
                tf_marca.setText(prod.p1.getMarca());
                tf_nome.setText(prod.p1.getNome());
                tf_tipo.setText(prod.p1.getTipo());
                tf_valor.setText(String.valueOf(prod.p1.getPreco()));
            }
            else{
                JOptionPane.showMessageDialog(null, "produto não localizado");
                limpaCampos();
            }
        }
