    private void bt_adicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_adicionarActionPerformed
        if(tf_marca.getText().equals("")){
            JOptionPane.showMessageDialog(null, "campo marca vazio!");
            tf_marca.requestFocus();
            return;
        }
        if(tf_nome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "campo nome vazio!");
            tf_nome.requestFocus();
            return;
        }
        
        if(tf_tipo.getText().equals("")){
            JOptionPane.showMessageDialog(null, "campo tipo vazio!");
            tf_tipo.requestFocus();
            return;
        }

        if(tf_valor.getText().equals("")){
            JOptionPane.showMessageDialog(null, "campo valor vazio!");
            tf_nome.requestFocus();
            return;
        }
       
          prod.p1.setMarca(tf_marca.getText());
          prod.p1.setNome(tf_nome.getText());
          prod.p1.setPreco(Double.parseDouble(tf_valor.getText()));
          prod.p1.setTipo(tf_tipo.getText());

        JOptionPane.showMessageDialog(null, prod.atualizar(produtosDAO.INCLUSAO));
        limpaCampos();
    }//GEN-LAST:event_bt_adicionarActionPerformed

