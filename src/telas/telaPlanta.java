    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed

        plant.p1.setId(Integer.parseInt(tf_Id.getText()));
        plant.p1.setNomeCie(tf_NomeCie.getText());
        plant.p1.setNomePop(tf_NomePop.getText());
        plant.p1.setPreco(Double.parseDouble(tf_Preco.getText()));
        plant.p1.setTamanho(Double.parseDouble(tf_NomeCie.getText()));
        JOptionPane.showMessageDialog(null, "dados atualizados." +plant.atualizar(plantaDAO.ALTERACAO));
        limpaCampos();
        habilitar(true, false, false, false, false, false, true, false, false);
    }//GEN-LAST:event_bt_salvarActionPerformed

