    private void bt_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_removerActionPerformed
        plant.p1.setId(Integer.parseInt(tf_Id.getText()));
        plant.localizar();
        int n = JOptionPane.showConfirmDialog(null,"deseja remover a planta de id " +plant.p1.getId()+" dos registros?", "apagar a planta?", JOptionPane.YES_NO_OPTION);
        if(n==JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, "removido! " +plant.atualizar(plantaDAO.EXCLUSAO));
            limpaCampos();
            habilitar(true, false, false, false, false, false, true, false, false);
        }
    }//GEN-LAST:event_bt_removerActionPerformed

