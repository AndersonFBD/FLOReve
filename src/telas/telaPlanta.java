    public void habilitar(boolean tfid, boolean tfnomep, boolean tfnomec,
            boolean tftipo, boolean tftam, boolean tfpreco,
            boolean beditar, boolean bremover, boolean bsalvar){
            tf_Id.setEnabled(tfid);
            tf_NomeCie.setEnabled(tfnomec);
            tf_NomePop.setEnabled(tfnomep);
            tf_Preco.setEnabled(tfpreco);
            tf_Tamanho.setEnabled(tftam);
            bt_editar.setEnabled(beditar);
            bt_remover.setEnabled(bremover);
            bt_salvar.setEnabled(bsalvar);
                
    }
    
    void limpaCampos(){
        tf_Id.setText("");
        tf_NomeCie.setText("");
        tf_NomePop.setText("");
        tf_Preco.setText("");
        tf_Tamanho.setText("");
    }
    
    public void atualizarCampos(){
        plant.p1.setId(Integer.parseInt(tf_Id.getText()));
        if (plant.localizar()){
            tf_Id.setText(String.valueOf(plant.p1.getId()));
            tf_NomeCie.setText(plant.p1.getNomeCie());
            tf_NomePop.setText(plant.p1.getNomePop());
            tf_Preco.setText(String.valueOf(plant.p1.getPreco()));
            tf_Tamanho.setText(String.valueOf(plant.p1.getTamanho()));
        }
        else{
            JOptionPane.showMessageDialog(null, "planta não encontrada");
            limpaCampos();
        }
    }
    
