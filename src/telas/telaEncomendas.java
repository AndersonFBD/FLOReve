    private void bt_removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_removerActionPerformed
        enc.en.setId(Integer.parseInt(tf_id.getText()));
        enc.localizar();
        int n = JOptionPane.showConfirmDialog(null,"deseja apagar a encomenda de registro " +enc.en.getId()+" dos registros?", "remover encomenda?", JOptionPane.YES_NO_OPTION);
        if(n==JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(null, "removido! " +enc.atualizar(encomendaPlantaDAO.EXCLUSAO));
            limpaCampos();
            habilitar(true, false, false, false, true, false, false);
        }
    }//GEN-LAST:event_bt_removerActionPerformed

    private void bt_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_editarActionPerformed
        if(tf_id.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "preencha o campo id");
        } else {
            habilitar(true,true,true,true,true,true,true);
        }
    }//GEN-LAST:event_bt_editarActionPerformed

