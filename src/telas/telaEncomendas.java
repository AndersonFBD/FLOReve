    private void habilitar (boolean tfid, boolean tfcli, boolean tfpla, boolean tfdata,
                            boolean beditar, boolean bremover, boolean bsalvar) {
        tf_id.setEnabled(tfid);
        tf_cliente.setEnabled(tfcli);
        tf_planta.setEnabled(tfpla);
        tf_data.setEnabled(tfdata);
        bt_editar.setEnabled(beditar);
        bt_remover.setEnabled(bremover);
        bt_salvar.setEnabled(bsalvar);
    }
    
    void limpaCampos(){
        tf_id.setText("");
        tf_cliente.setText("");
        tf_planta.setText("");
        tf_data.setText("");
        
    }
        
    public void atualizarCampos(){
        enc.en.setId(Integer.parseInt(tf_id.getText()));
        if (enc.localizar()){
            tf_id.setText(String.valueOf(enc.en.getId()));
            tf_cliente.setText(String.valueOf(enc.en.getIdCliente()));
            tf_planta.setText(String.valueOf(enc.en.getIdPlanta()));
            tf_data.setText(String.valueOf(enc.en.getData()));
        }
        else{
            JOptionPane.showMessageDialog(null, "encomenda não encontrada");
            limpaCampos();
        }
    }
    
