    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed

        enc.en.setId(Integer.parseInt(tf_id.getText()));
        enc.en.setIdCliente(Integer.parseInt(tf_cliente.getText()));
        enc.en.setIdPlanta(Integer.parseInt(tf_planta.getText()));
        enc.en.setData(tf_data.getText());
        JOptionPane.showMessageDialog(null, "dados atualizados." +enc.atualizar(encomendaPlantaDAO.ALTERACAO));
        limpaCampos();
        habilitar(true, false, false, false, true, false, false);
    }//GEN-LAST:event_bt_salvarActionPerformed

